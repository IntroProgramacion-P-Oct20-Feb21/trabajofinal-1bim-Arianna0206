"""
   Ejercicio03
   Calcular valor de la planilla de teléfono de un casa
"""

costo = float(input("Ingrese el costo por minuto: "))
número = float(input("Ingrese el número de minutos consumidos por el mes: ")) 
valor = float(costo)*float(número)
print("el valor de planilla es: " + str(valor))
        
        
